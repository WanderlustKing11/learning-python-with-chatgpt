# Basic I/O

# Input/Output operations are essential for any 
# program that interacts with users or handles data.

# Printing a simple string
print("Hello, Python!")

# Printing multiple items
x = 10
y = 20
print("The values of x and y are", x, "and", y, "respectively.")

# Reading Input: The input Function
# The input function is used to take input from 
# the user. It reads the user's input as a string.

# Reading user input
name = input("Enter your name: ")
print("Hello,", name)

# Reading numeric input
age = input("Enter your age: ")
age = int(age)  # Converting the string to an integer
print("You are", age, "years old.")

